x = 16
y = 3

# Arithmetic Operators
print("=== Arithmetic Operators ===")
print("x + y =", x + y)   # Addition
print("x - y =", x - y)   # Subtraction
print("x * y =", x * y)   # Multiplication
print("x / y =", x / y)   # Division (float)
print("x // y =", x // y) # Floor Division (no decimal)
print("x % y =", x % y)   # Modulus (remainder)
print("x ** y =", x ** y) # Exponent (16 to the power 3)

# Comparison Operators
print("\n=== Comparison Operators ===")
print("x == y :", x == y)  # Equal to
print("x != y :", x != y)  # Not equal to
print("x > y  :", x > y)   # Greater than
print("x < y  :", x < y)   # Less than
print("x >= y :", x >= y)  # Greater than or equal
print("x <= y :", x <= y)  # Less than or equal

# Logical Operators
print("\n=== Logical Operators ===")
print("x > 10 and y > 1  :", x > 10 and y > 1)   # Both must be True
print("x > 10 or y > 10  :", x > 10 or y > 10)   # At least one True
print("not(x > 10)       :", not(x > 10))         # Reverses the result